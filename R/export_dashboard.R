blink_index_html <- function(title) {
  title <- blink_escape_html(title %||% "BlinkDash")
  paste0(
'<!doctype html>
<html lang="en">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <meta name="generator" content="blinkdash">
  <title>', title, '</title>
  <link rel="icon" href="', blink_favicon_data_uri(), '">
  <link rel="stylesheet" href="assets/blinkdash.css">
